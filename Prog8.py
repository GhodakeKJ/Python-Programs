#Prog8.py
def show(a):  #Function Defination-1
	print("{}".format(a))
#Main Program	
show(10) #Function Call


def show(a,b): #Function Defination-2
	print("{}\t{}".format(a,b))
#Main Program	
show(10,20) #Function Call



def show(a,b,c): #Function Defination-3
	print("{}\t{}\t{}".format(a,b,c))
#Main Program	
show(10,20,30) #Function Call

def show(a,b,c,d): #Function Defination-4
	print("{}\t{}\t{}\t{}".format(a,b,c,d))
#Main Program	
show(10,20,30,40) #Function Call
